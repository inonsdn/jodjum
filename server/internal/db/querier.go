package db

import (
	"context"

	"github.com/jackc/pgx/v5"
	"github.com/jackc/pgx/v5/pgconn"
)

// Querier is the subset of query methods shared by *pgxpool.Pool and pgx.Tx.
// Repository methods that accept a Querier can run either directly on the pool
// (the normal case) or inside a transaction (when several writes must commit or
// roll back together).
type Querier interface {
	Query(ctx context.Context, sql string, args ...any) (pgx.Rows, error)
	QueryRow(ctx context.Context, sql string, args ...any) pgx.Row
	Exec(ctx context.Context, sql string, args ...any) (pgconn.CommandTag, error)
}
